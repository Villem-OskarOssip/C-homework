/**
	Funktsioon selgitab välja milline on antud kahest arvust väiksem
	\param [in] a esimene parameeter
	\param [in] b teine parameeter
	\return väiksem väärtus
*/
long minimum (long a, long b); 

/**
	Funktsioon selgitab välja milline on antud kahest arvust suurem
	\param [in] a esimene parameeter
	\param [in] b teine parameeter
	\return suurim väärtus
*/
long maximum (long a, long b);

/**
	Funktsioon selgitab välja milline on antud kahe arvu korrutis
	\param [in] a esimene parameeter
	\param [in] b teine parameeter
	\return korrutise
*/
long product (long a, long b);

/**
	Funktsioon selgitab välja milline on antud kahe arvu ühis suurima ühisteguri
	\param [in] a esimene parameeter
	\param [in] b teine parameeter
	\return a ja b suurim ühistegur
*/
long gcd (long a, long b);

/**
	Funktsioon selgitab välja milline on antud kahest arvu aste.
	\param [in] a esimene parameeter
	\param [in] b teine parameeter
	\return parameetrite astme
*/
long power (long a, long b);
